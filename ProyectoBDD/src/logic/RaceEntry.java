package logic;

public class RaceEntry {
	
	private int GateNumber;
	private String JockeyName; 
	private String HorseName; 
	private int Margins; 
	private int FinishPositon; 
	private int TimeElapsed; 
	private int AmountWon; 
	private int MorningLineOdds; 
	private int FinalOdds;
	
	//	*** Getters & Setters****
	public int getGateNumber() {
		return GateNumber;
	}
	public void setGateNumber(int gateNumber) {
		GateNumber = gateNumber;
	}
	public String getJockeyName() {
		return JockeyName;
	}
	public void setJockeyName(String jockeyName) {
		JockeyName = jockeyName;
	}
	public String getHorseName() {
		return HorseName;
	}
	public void setHorseName(String horseName) {
		HorseName = horseName;
	}
	public int getMargins() {
		return Margins;
	}
	public void setMargins(int margins) {
		Margins = margins;
	}
	public int getFinishPositon() {
		return FinishPositon;
	}
	public void setFinishPositon(int finishPositon) {
		FinishPositon = finishPositon;
	}
	public int getTimeElapsed() {
		return TimeElapsed;
	}
	public void setTimeElapsed(int timeElapsed) {
		TimeElapsed = timeElapsed;
	}
	public int getAmountWon() {
		return AmountWon;
	}
	public void setAmountWon(int amountWon) {
		AmountWon = amountWon;
	}
	public int getMorningLineOdds() {
		return MorningLineOdds;
	}
	public void setMorningLineOdds(int morningLineOdds) {
		MorningLineOdds = morningLineOdds;
	}
	public int getFinalOdds() {
		return FinalOdds;
	}
	public void setFinalOdds(int finalOdds) {
		FinalOdds = finalOdds;
	} 

}

